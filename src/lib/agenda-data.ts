export const groupedAgenda = agendaData.reduce((acc, event) => {
  const day = event.Dia;
  if (!acc[day]) {
    acc[day] = [];
  }
  acc[day].push(event);
  return acc;
}, {} as Record<string, AgendaEvent[]>);


